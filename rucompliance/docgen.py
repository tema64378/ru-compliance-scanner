"""Генератор документов для соответствия 152-ФЗ.

Собирает по данным оператора готовые тексты:
  • Политику обработки персональных данных (структура по 152-ФЗ + рекомендации РКН)
  • Согласие на обработку ПДн (для форм на сайте)
  • Политику в отношении файлов cookie

⚠️ Это шаблоны на основе типовой структуры 152-ФЗ. Их нужно проверить и
адаптировать под себя; это не заменяет юридическую консультацию.
"""

from dataclasses import dataclass, field
from datetime import date
from typing import List


@dataclass
class Operator:
    org_type: str = "ООО"          # ООО / ИП / Самозанятый / Физлицо
    name: str = ""                 # «ООО Ромашка» / ИП Иванов И.И.
    inn: str = ""
    ogrn: str = ""
    address: str = ""
    email: str = ""
    phone: str = ""
    site: str = ""
    data: List[str] = field(default_factory=lambda: ["фамилия, имя, отчество", "адрес электронной почты"])
    purposes: List[str] = field(default_factory=lambda: ["обратная связь с пользователем"])
    uses_cookie: bool = True
    uses_analytics: bool = True


def _li(items):
    return "\n".join(f"- {x}" for x in items)


def privacy_policy(op: Operator) -> str:
    today = date.today().strftime("%d.%m.%Y")
    reqs = f"{op.name}"
    details = []
    if op.ogrn:
        details.append(f"ОГРН{'ИП' if op.org_type == 'ИП' else ''}: {op.ogrn}")
    if op.inn:
        details.append(f"ИНН: {op.inn}")
    if op.address:
        details.append(f"адрес: {op.address}")
    det = (", ".join(details)) if details else ""
    analytics = ("\nДля сбора статистики используются сервисы веб-аналитики "
                 "(в т.ч. с использованием файлов cookie)." if op.uses_analytics else "")
    cookie_section = ""
    if op.uses_cookie:
        cookie_section = ("## 10. Файлы cookie\n"
                          "Сайт использует файлы cookie. Продолжая пользоваться сайтом, "
                          "пользователь соглашается с их использованием; настройки cookie "
                          "можно изменить в браузере.\n\n")
    final_num = "11" if op.uses_cookie else "10"
    return f"""# Политика в отношении обработки персональных данных

_Редакция от {today}_

## 1. Общие положения
Настоящая Политика определяет порядок обработки персональных данных и меры по
обеспечению их безопасности в **{reqs}**{(' (' + det + ')') if det else ''}
(далее — Оператор) и действует в отношении информации, получаемой Оператором о
пользователях сайта {op.site or '(адрес сайта)'}.

Политика разработана в соответствии с Федеральным законом от 27.07.2006 № 152-ФЗ
«О персональных данных».

## 2. Основные понятия
- **Персональные данные** — любая информация, относящаяся к прямо или косвенно
  определённому физическому лицу (субъекту персональных данных).
- **Обработка персональных данных** — любое действие с персональными данными
  (сбор, запись, хранение, использование, передача, удаление и т.д.).
- **Оператор** — лицо, организующее и осуществляющее обработку персональных данных.

## 3. Правовые основания обработки
Оператор обрабатывает персональные данные на основании:
- согласия субъекта на обработку его персональных данных;
- Федерального закона № 152-ФЗ и иных нормативных правовых актов РФ.

## 4. Цели обработки персональных данных
{_li(op.purposes)}

## 5. Категории субъектов и обрабатываемых данных
Субъекты — пользователи сайта {op.site or '(адрес сайта)'}. Оператор обрабатывает
следующие персональные данные:
{_li(op.data)}{analytics}

## 6. Порядок и условия обработки
Обработка осуществляется с согласия субъекта, законными и справедливыми способами.
Оператор не раскрывает третьим лицам и не распространяет персональные данные без
согласия субъекта, если иное не предусмотрено законом. **Базы данных, содержащие
персональные данные граждан РФ, размещаются на серверах, расположенных на
территории Российской Федерации** (ст. 18 152-ФЗ).

## 7. Сроки обработки и хранения
Персональные данные обрабатываются до достижения целей обработки либо до отзыва
согласия субъектом, после чего подлежат уничтожению или обезличиванию.

## 8. Права субъекта персональных данных
Субъект вправе:
- получать информацию об обработке своих персональных данных;
- требовать уточнения, блокирования или уничтожения данных;
- отозвать согласие на обработку;
- обжаловать действия Оператора в Роскомнадзоре или в суде.

Запросы направляются Оператору по адресу: {op.email or '(e-mail оператора)'}.

## 9. Меры по обеспечению безопасности
Оператор принимает правовые, организационные и технические меры защиты
персональных данных от неправомерного доступа, в том числе использует защищённое
соединение (HTTPS) и ограничивает доступ к данным.

{cookie_section}## {final_num}. Заключительные положения
Оператор вправе вносить изменения в настоящую Политику. Актуальная редакция
размещается на сайте {op.site or '(адрес сайта)'}.
Контакты Оператора: {op.email or '(e-mail)'}{(', ' + op.phone) if op.phone else ''}.
"""


def consent_text(op: Operator) -> str:
    return f"""# Согласие на обработку персональных данных

Я даю согласие **{op.name or '(Оператор)'}** на обработку моих персональных данных
({', '.join(op.data)}), указанных мной в форме на сайте {op.site or '(адрес сайта)'},
в целях: {', '.join(op.purposes)}.

Согласие даётся на совершение действий, предусмотренных Федеральным законом
№ 152-ФЗ, и действует до его отзыва. Отзыв направляется на {op.email or '(e-mail)'}.
С [Политикой обработки персональных данных]({op.site or '#'}/privacy) ознакомлен(а).

---
**Как поставить на сайт:** рядом с каждой формой добавьте НЕпредзаполненный чекбокс:

```html
<label>
  <input type="checkbox" name="pd_consent" required>
  Я согласен(а) на обработку персональных данных и принимаю
  <a href="/privacy" target="_blank">Политику обработки ПДн</a>
</label>
```
"""


def cookie_policy(op: Operator) -> str:
    purpose_word = ", аналитики и" if op.uses_analytics else " и"
    analytics_bullet = "\n- аналитические (статистика посещений);" if op.uses_analytics else ""
    analytics_note = ("Аналитические cookie устанавливаются только после согласия. "
                      if op.uses_analytics else "")
    return f"""# Политика в отношении файлов cookie

Сайт {op.site or '(адрес сайта)'} использует файлы cookie — небольшие текстовые
файлы, сохраняемые в браузере, для корректной работы сайта{purpose_word}
улучшения пользовательского опыта.

**Типы cookie:**
- технические (необходимы для работы сайта);{analytics_bullet}
- функциональные (запоминание настроек).

**Согласие.** При первом визите пользователю показывается баннер с выбором
«Принять / Отклонить». {analytics_note}Отказаться можно также в настройках браузера.

Контакты по вопросам обработки данных: {op.email or '(e-mail)'}.
"""


def generate_all(op: Operator) -> dict:
    return {
        "privacy_policy": privacy_policy(op),
        "consent": consent_text(op),
        "cookie_policy": cookie_policy(op),
    }
